public class JavaBenefits {
    /**
     * Java has some key benefits that you'll need to k now for the exam:
     * 
     * Object Oriented 
     * Java is an object-oriented language, which means all code is
     * defined in classes and most of those classes can be instantiated into
     * objects. We'll discuss this more throughout the book. Many languages
     * before Java were procedural, which meant there were routines or methods
     * but no classes. Another common approach is functional programming. Java
     * allows for functional programming within a class, but object oriented is
     * still the main organization of code. 
     * 
     * Encapsulation 
     * Java supports access modifiers to protect data from
     * unintended access and modification. Most people consider encapsulation to
     * be an aspect of object-oriented languages. Since the exam objectives call
     * attention to it specifically, so do we.
     * 
     * Platform Independent 
     * Java is an interpreted language because it gets
     * compiled to bytecode. A key benefit is that Java code gets compiled once
     * rather than needing to be recompiled for different operating systems.
     * This is known as “write once, run everywhere.” On the OCP exam, you'll
     * learn that it is possible to write code that does not run everywhere. For
     * example, you might refer to a file in a specific directory. If you get
     * asked on the OCA exam, the answer is that the same class files run
     * everywhere.
     * 
     * Robust 
     * One of the major advantages of Java over C++ is that it prevents
     * memory leaks. Java manages memory on its own and does garbage collection
     * automatically. Bad memory management in C++ is a big source of errors in
     * programs.
     * 
     * Simple
     * Java was intended to be simpler than C++. In addition to
     * eliminating pointers, it got rid of operator overloading. In C++, you
     * could write a + b and have it mean almost anything.
     * 
     * Secure 
     * Java code runs inside the JVM. This creates a sandbox that makes
     * it hard for Java code to do evil things to the computer it is running on.
     */
}
